print(f"  {'Category':<16s} {'n':>6s} {'Mean':>8s} {'Median':>8s} {'Std':>8s}")
print(f"  {'─'*50}")
for cat in PLOT_ORDER_C:
    vals = metadata.loc[metadata['cat7'] == cat, 'sil'].values
    print(f"  {cat:<16s} {len(vals):>6d} {vals.mean():>+8.4f} {np.median(vals):>+8.4f} {vals.std():>8.4f}")
print(f"  Overall 7-class Silhouette: {sil_overall:.4f}")

print(f"\n{'─'*70}")
print("KEY MESSAGES:")
print("  1. Extreme proteins have negative silhouette → topologically mixed")
