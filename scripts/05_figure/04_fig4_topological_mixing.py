print("[Panel B] Slope chart: Full-length vs Region Silhouette (7-class, 3 extreme subcategories)")
print(f"{'─'*70}")
print(f"  {'Category':<16s} {'FL Sil':>8s} {'Rg Sil':>8s} {'Change':>10s}")
print(f"  {'─'*46}")
for d in slope_rows:
    direction = '+' if d['chg'] > 0 else ''
    print(f"  {d['label']:<16s} {d['fl']:>+8.3f} {d['rg']:>+8.3f} {direction}{d['chg']:.1f}%")
print(f"  → Only IDP/Fold-switching/Knotted have region annotations")
