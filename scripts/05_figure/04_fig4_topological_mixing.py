for d in slope_rows:
    direction = '+' if d['chg'] > 0 else ''
    print(f"     {d['label']:<16s}: {d['fl']:+.3f} → {d['rg']:+.3f} ({direction}{d['chg']:.0f}%)")
print("  3. Only Random has strong positive silhouette (grammar violation)")
print("  4. Knotted most negative → knot topology invisible to ESM-2")
